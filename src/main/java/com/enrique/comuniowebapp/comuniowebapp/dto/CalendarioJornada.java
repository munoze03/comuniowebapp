package com.enrique.comuniowebapp.comuniowebapp.dto;

public class CalendarioJornada {

    private String jornada;
    private String urlHome;
    private String urlAway;
    private String fechaHora;
    private String urlTV;
    public String getUrlHome() {
        return urlHome;
    }
    public void setUrlHome(String urlHome) {
        this.urlHome = urlHome;
    }
    public String getUrlAway() {
        return urlAway;
    }
    public void setUrlAway(String urlAway) {
        this.urlAway = urlAway;
    }
    public String getFechaHora() {
        return fechaHora;
    }
    public void setFechaHora(String fechaHora) {
        this.fechaHora = fechaHora;
    }
    public String getUrlTV() {
        return urlTV;
    }
    public void setUrlTV(String urlTV) {
        this.urlTV = urlTV;
    }
    public String getJornada() {
        return jornada;
    }
    public void setJornada(String jornada) {
        this.jornada = jornada;
    }

    

}
